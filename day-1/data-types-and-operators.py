# Data Types and Operators

# In Python the main data types are:
# int --> Integer, whole numbers (1, 54, -70)
# float --> Decimal number (3.14, 0.5, -7.38)
# str --> String ("hello", "World!", "80")
# bool --> Boolean, logical values (True, False)

# Casting --> Means changing one type into another
my_num = 5 # int
my_float = float(my_num) # 5.0
print(my_float)

my_str = "123"
my_int = int(my_str)
print(my_int)

